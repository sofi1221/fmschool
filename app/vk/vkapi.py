def upload_photo(vk_session, event, x):
    upload = vk_api.VkUpload(vk_session)
    for i in range(1, 3):
        photo = upload.photo_messages([f'timetable\{x}{i}.jpg'])
        vk_photo_id = f"photo{photo[0]['owner_id']}_{photo[0]['id']}"
        vk = vk_session.get_api()
        vk.messages.send(user_id=event.obj.message['from_id'],
                         attachment=vk_photo_id, message='',
                         random_id=random.randint(0, 2 ** 64))


def timet(vk, longpoll, vk_session):
    for event in longpoll.listen():
        text = event.obj.message['text'].lower()
        if event.type == VkBotEventType.MESSAGE_NEW:
            if '—Å—Ç–æ–ø' in text:
                break
            if ('–ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫' in text) or ('–ø–Ω' in text):
                upload_photo(vk_session, event, '–ø–Ω')
            elif ('–≤—Ç' in text) or ('–≤—Ç–æ—Ä–Ω–∏–∫' in text):
                upload_photo(vk_session, event, '–≤—Ç')
            elif ('—Å—Ä' in text) or ('—Å—Ä–µ–¥' in text):
                upload_photo(vk_session, event, '—Å—Ä')
            elif ('—á—Ç' in text) or ('—á–µ—Ç–≤–µ—Ä–≥' in text):
                upload_photo(vk_session, event, '—á—Ç')
            elif ('–ø—Ç' in text) or ('–ø—è—Ç–Ω–∏—Ü' in text):
                upload_photo(vk_session, event, '–ø—Ç')
            elif ('—Å–±' in text) or ('—Å—É–±–±–æ—Ç' in text) or ('–≤—Å' in text) or ('–≤–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ' in text):
                vk.messages.send(user_id=event.obj.message['from_id'],
                                 message='–ö–∞–π—Ñ! –£—Ä–æ–∫–æ–≤ –Ω–µ—Ç!',
                                 random_id=random.randint(0, 2 ** 64))
            else:
                vk.messages.send(user_id=event.obj.message['from_id'],
                                 message='–ö–∞–∫–æ–π –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏?',
                                 random_id=random.randint(0, 2 ** 64))


